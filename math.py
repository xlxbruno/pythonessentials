# in built math functions
# max and min : returns max value or min value in a collection
import math

x = max(100, 200, 300)
y = min(2, 83, 248)
print(x)
print(y)

# abs : absolute value
x = abs(-31)
print(x)

# pow : powers of values, pow(number,power)
x = pow(4,3)
print(x)

# math module
# to include a module we use import
# a module is simply file that can be implemented in other classes
# eg sqrt

x = math.sqrt(64)
print(x)

# rounding of: round up(math.ceil), round down(math.floor)
x = math.ceil(2.4)
y = math.floor(2.4)
print(x)
print(y)

